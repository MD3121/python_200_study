# 68. 반올림 수 구하기 (round)

ret1 = round(1118)                  # 소수점 첫째 자리에서 반올림
ret2 = round(16.554)                # 소수점 첫째 자리에서 반올림
ret3 = round(1118,-1)               # 1의 자리에서 반올림 해줌               
ret4 = round(16.554,2)              # 소수점 셋째 자리에서 반올림 해줌
print(ret1)
print(ret2)
print(ret3)
print(ret4)